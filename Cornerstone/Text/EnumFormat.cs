namespace Cornerstone.Text;

/// <summary>
/// The format to store enums in. Defaults to EnumFormat.Value.
/// </summary>
public enum EnumFormat
{
	/// <summary>
	/// The numeric value of the enum
	/// </summary>
	Value = 0,

	/// <summary>
	/// The string value of the enum name
	/// </summary>
	Name = 1
}