namespace Cornerstone.Text;

/// <summary>
/// Represents naming convention options.
/// </summary>
public enum NamingConvention
{
	/// <summary>
	/// Pascal Case (PascalCase)
	/// </summary>
	PascalCase = 0,

	/// <summary>
	/// Camel Case (camelCase)
	/// </summary>
	CamelCase = 1,

	/// <summary>
	/// Kebab Case (Kebab-Case)
	/// </summary>
	KebabCase = 2,

	/// <summary>
	/// Snake Case (Snake-Case)
	/// </summary>
	SnakeCase = 3
}