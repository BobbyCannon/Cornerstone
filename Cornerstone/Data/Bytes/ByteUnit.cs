namespace Cornerstone.Data.Bytes;

/// <summary>
/// Units of number for byte data
/// </summary>
public enum ByteUnit
{
	/// <summary>
	/// Bit
	/// </summary>
	Bit = 0,

	/// <summary>
	/// Byte
	/// </summary>
	Byte = 1,

	/// <summary>
	/// Kilobit
	/// </summary>
	Kilobit = 2,

	/// <summary>
	/// Kilobyte
	/// </summary>
	Kilobyte = 3,

	/// <summary>
	/// Megabit
	/// </summary>
	Megabit = 4,

	/// <summary>
	/// Megabyte
	/// </summary>
	Megabyte = 5,

	/// <summary>
	/// Gigabit
	/// </summary>
	Gigabit = 6,

	/// <summary>
	/// Gigabyte
	/// </summary>
	Gigabyte = 7,

	/// <summary>
	/// Terabit
	/// </summary>
	Terabit = 8,

	/// <summary>
	/// Terabyte
	/// </summary>
	Terabyte = 9
}