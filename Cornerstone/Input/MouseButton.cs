namespace Cornerstone.Input;

/// <summary>
/// The mouse button
/// </summary>
public enum MouseButton
{
	/// <summary>
	/// Left mouse button
	/// </summary>
	LeftButton,

	/// <summary>
	/// Middle mouse button
	/// </summary>
	MiddleButton,

	/// <summary>
	/// Right mouse button
	/// </summary>
	RightButton,

	/// <summary>
	/// 4th mouse button. Typically, performs the same function as BrowserBack.
	/// </summary>
	XButton1,

	/// <summary>
	/// 5th mouse button. Typically, performs the same function as BrowserForward.
	/// </summary>
	XButton2
}