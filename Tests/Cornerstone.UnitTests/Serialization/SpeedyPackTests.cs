			(
				"Enum (int)",

				// 89 (x59), 65 (x41)
				[ConsoleKey.Y, ConsoleKey.A],

				//                 89                        65
				[0x23, 0x23, 0xFF, 0x59, 0x00, 0x00, 0x00, 0x41, 0x00, 0x00, 0x00]
			),
			(
				"boolean",
				[true, false],
				[0x2, 0x3, 0xFF]
			),
			(
				"char",
				[char.MinValue, char.MaxValue, ' '],
