	#region Methods

	[Test]
	public void AllBasicTypes()
	{
		var scenarios = new (string Name, object[] Value, byte[] Expected)[]
		{
			(
				"Enum (long)",
				[EnumUsingLong.Min, EnumUsingLong.Zero, EnumUsingLong.Max],
