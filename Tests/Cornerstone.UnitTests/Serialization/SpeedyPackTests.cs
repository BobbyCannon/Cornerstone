				[
					DateTime.MinValue, DateTime.MaxValue, new DateTime(DateTimeExtensions.UnixEpochDateTimeTicks),
					new DateTime(DateTimeExtensions.WindowsEpochDateTimeTicks), StartDateTime
				],
				[0x7A, 0x7B, 0x7C, 0x7D, 0x79, 0xFF, 0x00, 0x50, 0xA8, 0x26, 0xE5, 0x22, 0xC1, 0x08]
			),
			(
				"DateTimeOffset",
				[DateTimeOffset.MinValue, DateTimeOffset.MaxValue, new DateTimeOffset(DateTimeExtensions.UnixEpochDateTimeTicks, TimeSpan.Zero), new DateTimeOffset(DateTimeExtensions.WindowsEpochDateTimeTicks, TimeSpan.Zero), new DateTimeOffset(StartDateTime, TimeSpan.Zero)],
				[0x7F, 0x80, 0x81, 0x82, 0x7E, 0xFF, 0x00, 0x50, 0xA8, 0x26, 0xE5, 0x22, 0xC1, 0x08, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00]
			),
			(
				"TimeOnly",
				[TimeOnly.MinValue, TimeOnly.MaxValue, new TimeOnly(StartDateTime.TimeOfDay.Ticks)],
				[0x84, 0x85, 0x83, 0xFF, 0x00, 0x50, 0x5A, 0xB4, 0x19, 0x00, 0x00, 0x00]
			),
			(
				"TimeSpan",
				[TimeSpan.MinValue, TimeSpan.MaxValue, StartDateTime.TimeOfDay],
				[0x87, 0x88, 0x86, 0xFF, 0x00, 0x50, 0x5A, 0xB4, 0x19, 0x00, 0x00, 0x00]
			),
			(
				"Guid",
				[Guid.Empty, Guid.AllBitsSet, Guid.Parse("CD5CA8C0-B1A5-4090-A2F9-7DE7C88B9EC3")],
				[0x8B, 0x8C, 0x8A, 0xFF, 0xC0, 0xA8, 0x5C, 0xCD, 0xA5, 0xB1, 0x90, 0x40, 0xA2, 0xF9, 0x7D, 0xE7, 0xC8, 0x8B, 0x9E, 0xC3]
			),
			(
				"Version",
				[
					new Version(), new Version(1, 0), new Version(1, 0, 0), new Version(1, 0, 0, 0), new Version(1, 2, 3, 4),
					new Version(int.MaxValue, int.MaxValue, int.MaxValue, int.MaxValue)
				],
				[0x8D, 0x8E, 0x8F, 0x90, 0x8D, 0x8D, 0xFF, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0x01, 0x00, 0x00, 0x00, 0x02, 0x00, 0x00, 0x00, 0x03, 0x00, 0x00, 0x00, 0x04, 0x00, 0x00, 0x00, 0xFF, 0xFF, 0xFF, 0x7F, 0xFF, 0xFF, 0xFF, 0x7F, 0xFF, 0xFF, 0xFF, 0x7F, 0xFF, 0xFF, 0xFF, 0x7F]
			)
		};

		foreach (var scenario in scenarios)
		{
			var packed = SpeedyPacket.Pack(scenario.Value).ToArray();
			packed.DumpHexArray(scenario.Name + ": [", "]");
			AreEqual(scenario.Expected, packed);

			packed = SpeedyPacket.Pack((IEnumerable) scenario.Value).ToArray();
			AreEqual(scenario.Expected, packed);

			var unpackaged = SpeedyPacket.Unpack(packed);
			AreEqual(scenario.Value, unpackaged.ToArray());
		}
	}

	#endregion
}