			),
			(
				"sbyte (UInt8)",
				[sbyte.MinValue, sbyte.MaxValue, (sbyte) -1, (sbyte) 0, (sbyte) 1, (sbyte) 2, (sbyte) 0x55],
				[0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x10, 0xFF, 0x55]
			),
			(
				"short (Int16)",
				[short.MinValue, short.MaxValue, (short) -1, (short) 0, (short) 1, (short) 2, short.MaxValue / 2],
				[0x18, 0x19, 0x1A, 0x1B, 0x1C, 0x1D, 0x23, 0xFF, 0xFF, 0x3F, 0x00, 0x00]
			),
			(
				"ushort (UInt16)",
				[ushort.MinValue, ushort.MaxValue, (ushort) 1, (ushort) 2, ushort.MaxValue / 2],
				[0x1F, 0x20, 0x21, 0x22, 0x23, 0xFF, 0xFF, 0x7F, 0x00, 0x00]
			),
			(
				"int (Int32)",
				[int.MinValue, int.MaxValue, -1, 0, 1, 2, int.MaxValue / 2],
				[0x24, 0x25, 0x26, 0x27, 0x28, 0x29, 0x23, 0xFF, 0xFF, 0xFF, 0xFF, 0x3F]
