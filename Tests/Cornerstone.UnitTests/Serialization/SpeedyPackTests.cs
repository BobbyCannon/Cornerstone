			(
				"long (Int64)",
				[long.MinValue, long.MaxValue, (long) -1, (long) 0, (long) 1, (long) 2, long.MaxValue / 2],
				[0x30, 0x31, 0x32, 0x33, 0x2F, 0x2F, 0x2F, 0xFF, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x02, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0x3F]
			),
			(
				"ulong (UInt64)",
				[ulong.MinValue, ulong.MaxValue, (ulong) 1, (ulong) 2, ulong.MaxValue / 2],
				[0x37, 0x38, 0x39, 0x3A, 0x36, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0x7F]
			),
			(
				"Int128",
				[Int128.MinValue, Int128.MaxValue, (Int128) (-1), (Int128) 0, (Int128) 1, (Int128) 2, Int128.MaxValue / 2],
				[0x3C, 0x3D, 0x3E, 0x3F, 0x40, 0x41, 0x3B, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0x3F]
			),
			(
				"UInt128",
				[UInt128.MinValue, UInt128.MaxValue, (UInt128) 1, (UInt128) 2, UInt128.MaxValue / 0x2],
				[0x43, 0x44, 0x45, 0x46, 0x42, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0x7F]
