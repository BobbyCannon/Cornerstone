#region References

using System;
using System.Collections;
using System.Linq;
using Cornerstone.Extensions;
using Cornerstone.Serialization;
using Cornerstone.Testing;
using NUnit.Framework;

#endregion

namespace Cornerstone.UnitTests.Serialization;

public class SpeedyPackTests : CornerstoneUnitTest
{
	#region Methods

	[Test]
	public void AllBasicTypes()
	{
		var scenarios = new (string Name, object[] Value, byte[] Expected)[]
		{
			(
				"boolean",
				[true, false],
				[0x2, 0x3, 0xFF]
			),
			(
				"character",
				[char.MinValue, char.MaxValue, ' '],
				[0x8, 0x9, 0x7, 0xFF, 0x20, 0x0]
			),
			(
				"Strings",
				[string.Empty, null, "foo", "u: \u3250"],
				[0x06, 0x04, 0x05, 0x05, 0xFF, 0x02, 0x66, 0x6F, 0x6F, 0x03, 0x02, 0x75, 0x3A, 0x20, 0x5C, 0x75, 0x33, 0x32, 0x35, 0x30, 0x03]
			),
			(
				"byte",
				[byte.MinValue, byte.MaxValue, (byte) 0x55],
				[0xB, 0xC, 0xA, 0xFF, 0x55]
			),
			(
				"sbyte",
				[sbyte.MinValue, sbyte.MaxValue, (sbyte) 0x55],
				[0x10, 0x11, 0xF, 0xFF, 0x55]
			),
			(
				"short",
				[short.MinValue, short.MaxValue, (short) 0x0],
				[0x16, 0x17, 0x15, 0xFF, 0x0, 0x0]
			),
			(
				"ushort",
				[ushort.MinValue, ushort.MaxValue, (ushort) 0x7FFF],
				[0x1C, 0x1D, 0x1B, 0xFF, 0xFF, 0x7F]
			),
			(
				"int",
				[int.MinValue, int.MaxValue, 0x0],
				[0x1F, 0x20, 0x1E, 0xFF, 0x0, 0x0, 0x0, 0x0]
			),
			(
				"uint",
				[uint.MinValue, uint.MaxValue, uint.MaxValue / 0x2],
				[0x22, 0x23, 0x21, 0xFF, 0xFF, 0xFF, 0xFF, 0x7F]
			),
			(
				"long",
				[long.MinValue, long.MaxValue, (long) 0x0],
				[0x25, 0x26, 0x24, 0xFF, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00]
			),
			(
				"ulong",
				[ulong.MinValue, ulong.MaxValue, ulong.MaxValue / 0x2],
				[0x28, 0x29, 0x27, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0x7F]
			),
			(
				"Int128",
				[Int128.MinValue, Int128.MaxValue, (Int128) 0x0],
				[0x2B, 0x2C, 0x2A, 0xFF, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00]
			),
			(
				"UInt128",
				[UInt128.MinValue, UInt128.MaxValue, UInt128.MaxValue / 0x2],
				[0x2E, 0x2F, 0x2D, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0x7F]
			),
			(
				"Float",
				[float.MinValue, float.MaxValue, float.E, float.Pi, float.Tau, float.NegativeInfinity, float.PositiveInfinity, float.NaN, float.NegativeZero, float.MaxValue / 0x2],
				[0x37, 0x38, 0x3D, 0x3F, 0x40, 0x39, 0x3A, 0x3B, 0x3C, 0x36, 0xFF, 0xFF, 0xFF, 0xFF, 0x7E]
			),
			(
				"Double",
				[double.MinValue, double.MaxValue, double.E, double.Pi, double.Tau, double.NegativeInfinity, double.PositiveInfinity, double.NaN, double.NegativeZero, double.MaxValue / 0x2],
				[0x42, 0x43, 0x49, 0x4B, 0x4C, 0x45, 0x46, 0x47, 0x48, 0x41, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xDF, 0x7F]
			),
			(
				"Decimal",
				[decimal.MinValue, decimal.MaxValue, decimal.Zero, decimal.One, decimal.MinusOne, decimal.MaxValue / 0x2],
				[0x4E, 0x4F, 0x50, 0x51, 0x52, 0x4D, 0xFF, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0x00, 0x00, 0x00, 0x00]
			),
			(
				"DateOnly",
				[DateOnly.MinValue, DateOnly.MaxValue, DateOnly.FromDayNumber(DateTimeExtensions.UnixEpochDateOnlyDayNumber), DateOnly.FromDayNumber(DateTimeExtensions.WindowsEpochDateOnlyDayNumber), DateOnly.FromDateTime(StartDateTime)],
				[0x54, 0x55, 0x56, 0x57, 0x53, 0xFF, 0x08, 0x24, 0x0B, 0x00]
			),
			(
				"DateTime",
				[DateTime.MinValue, DateTime.MaxValue, new DateTime(DateTimeExtensions.UnixEpochDateTimeTicks), new DateTime(DateTimeExtensions.WindowsEpochDateTimeTicks), StartDateTime],
				[0x59, 0x5A, 0x5B, 0x5C, 0x58, 0xFF, 0x00, 0x50, 0xA8, 0x26, 0xE5, 0x22, 0xC1, 0x08]
			),
			(
				"DateTimeOffset",
				[DateTimeOffset.MinValue, DateTimeOffset.MaxValue, new DateTimeOffset(DateTimeExtensions.UnixEpochDateTimeTicks, TimeSpan.Zero), new DateTimeOffset(DateTimeExtensions.WindowsEpochDateTimeTicks, TimeSpan.Zero), new DateTimeOffset(StartDateTime, TimeSpan.Zero)],
				[0x5E, 0x5F, 0x60, 0x61, 0x5D, 0xFF, 0x00, 0x50, 0xA8, 0x26, 0xE5, 0x22, 0xC1, 0x08, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00]
			),
			(
				"TimeOnly",
				[TimeOnly.MinValue, TimeOnly.MaxValue, new TimeOnly(StartDateTime.TimeOfDay.Ticks)],
				[0x63, 0x64, 0x62, 0xFF, 0x00, 0x50, 0x5A, 0xB4, 0x19, 0x00, 0x00, 0x00]
			),
			(
				"TimeSpan",
				[TimeSpan.MinValue, TimeSpan.MaxValue, StartDateTime.TimeOfDay],
				[0x66, 0x67, 0x65, 0xFF, 0x00, 0x50, 0x5A, 0xB4, 0x19, 0x00, 0x00, 0x00]
			),
			(
				"Guid",
				[Guid.Empty, Guid.AllBitsSet, Guid.Parse("CD5CA8C0-B1A5-4090-A2F9-7DE7C88B9EC3")],
				[0x6A, 0x6B, 0x69, 0xFF, 0xC0, 0xA8, 0x5C, 0xCD, 0xA5, 0xB1, 0x90, 0x40, 0xA2, 0xF9, 0x7D, 0xE7, 0xC8, 0x8B, 0x9E, 0xC3]
			),
			(
				"Version",
				[new Version(1, 2, 3, 4)],
				[0x6C, 0xFF, 0x01, 0x00, 0x00, 0x00, 0x02, 0x00, 0x00, 0x00, 0x03, 0x00, 0x00, 0x00, 0x04, 0x00, 0x00, 0x00]
			)
		};

		foreach (var scenario in scenarios)
		{
			var packed = SpeedyPacket.Pack(scenario.Value).ToArray();
			packed.DumpHexArray(scenario.Name + ": [", "]");
			AreEqual(scenario.Expected, packed);

			packed = SpeedyPacket.Pack((IEnumerable) scenario.Value).ToArray();
			AreEqual(scenario.Expected, packed);

			var unpackaged = SpeedyPacket.Unpack(packed);
			AreEqual(scenario.Value, unpackaged.ToArray());
		}
	}

	#endregion
}