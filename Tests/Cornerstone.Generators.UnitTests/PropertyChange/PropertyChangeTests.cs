	[TestMethod]
	public void NotifyAttributeWithoutPartial()
	{
		var source = """
					namespace Cornerstone.Test.Models;

					public partial class SampleRecord : Notifiable
					{
						[Notify]
						public string Value { get; set; }
					}
					""";

		// Generated Code - Expected
		var expected = new[]
		{
			"Error CSG002: Type \'SampleRecord.Value\' Property must be partial in order for Cornerstone.Generators to generate the property.",
			"Error CS9249: Partial property \'SampleRecord.Value\' must have a definition part.",
			"Error CS0102: The type \'SampleRecord\' already contains a definition for \'Value\'"
		};
		// Generated Code - /Expected

		ExpectErrors(source, expected);
	}

